# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution(object):
    def __init__(self):
        self.total = 0

    def convertBST(self, root):
        """
        :type root: TreeNode
        :rtype: TreeNode
        """
        if root:
            self.convertBST(root.right)
            self.total += root.val
            root.val = self.total
            self.convertBST(root.left)
        return root

    def convertBSTIterate(self, root):
        total = 0

        stack = []
        node = root

        while stack or node:
            while node:
                stack.append(node)
                node = node.right

            total += node.val
            node.val = total

            node = node.left

        return root


